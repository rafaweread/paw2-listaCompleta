package model;

public class UserBean {

    private int Id;
    private String nome;
    private String cpf;
    private String telefone;
    private String datanascimento;
    private String cidade;
    private String estado;
    private String diarias;     
    private String quantidadepessoas;

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getDatanascimento() {
        return datanascimento;
    }

    public void setDatanascimento(String datanascimento) {
        this.datanascimento = datanascimento;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getDiarias() {
        return diarias;
    }

    public void setDiarias(String diarias) {
        this.diarias = diarias;
    }

    public String getQuantidadepessoas() {
        return quantidadepessoas;
    }

    public void setQuantidadepessoas(String quantidadepessoas) {
        this.quantidadepessoas = quantidadepessoas;
    }

    

}
    

   